async function main() {
  // Find admin user to set as createdBy
  const admin = await prisma.user.findFirst({
    where: { userRoles: { some: { role: { name: "admin" } } } },
  });

  if (!admin) {
    console.error("Admin user not found. Run main seed first.");
    process.exit(1);
  }

  for (const card of cards) {
    // Avoid duplicates — skip if title already exists
    const existing = await prisma.knowledgeItem.findFirst({
      where: { title: card.title },
    });
    if (existing) {
      console.log(`  skip (exists): ${card.title}`);
      continue;
    }

    await prisma.knowledgeItem.create({
      data: {
        title: card.title,
        type: "ARTICLE",
        audience: "STAFF",
        tags: card.tags,
        description: card.description,
        content: card.content.trim(),
        createdById: admin.id,
      },
    });
    console.log(`  created: ${card.title}`);
  }

  console.log(`\nTraining cards seeded: ${cards.length} items (tag: "${TRAINING_TAG}")`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
