async function main() {
  // Find admin user
  const admin = await prisma.user.findFirst({
    where: { userRoles: { some: { role: { name: "admin" } } } },
  });
  if (!admin) {
    console.error("Admin user not found.");
    process.exit(1);
  }

  // Delete old separate training cards
  const oldCards = await prisma.knowledgeItem.findMany({
    where: { tags: { has: "Обучение" } },
    select: { id: true, title: true },
  });

  if (oldCards.length > 0) {
    await prisma.knowledgeItem.deleteMany({
      where: { id: { in: oldCards.map((c) => c.id) } },
    });
    console.log(`Deleted ${oldCards.length} old training cards`);
  }

  // Create single merged article
  const existing = await prisma.knowledgeItem.findFirst({
    where: { title: MERGED_TITLE },
  });

  if (existing) {
    await prisma.knowledgeItem.update({
      where: { id: existing.id },
      data: { content: MERGED_CONTENT },
    });
    console.log(`Updated existing: ${MERGED_TITLE}`);
  } else {
    await prisma.knowledgeItem.create({
      data: {
        title: MERGED_TITLE,
        type: "ARTICLE",
        audience: "STAFF",
        tags: ["Обучение", "Процессы"],
        description:
          "Полное руководство для сотрудников: вход, дашборд, организации, задачи, документы, сообщения, уведомления",
        content: MERGED_CONTENT,
        createdById: admin.id,
      },
    });
    console.log(`Created: ${MERGED_TITLE}`);
  }
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
